	<div class="title">Первый снег<span class="time">9 октября</div>
	<div class="img_left" img="images/news_1.jpg"></div>
	<p>По прогнозу синоптиков, в ближайшее время в столице выпадет первый снег, а значит, повысится аварийность на дорогах и число мелких аварий может возрасти в разы. В результате ситуация с пробками в Москве может незначительно усугубиться.</p>
	<p>На следующей неделе обещают первые снежные осадки, ветер должен смениться на северный, на дорогах из-за низкой температуры может образоваться ледяной покров. Поэтому в ГИББД Москвы предупреждают, чтобы водители учитывали погодные условия и были максимально осторожны на дорогах, а ткаже, по возможности, пересели на общественный транспорт. </p>
